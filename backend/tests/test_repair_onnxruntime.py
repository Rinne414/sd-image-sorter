from __future__ import annotations

import subprocess

import repair_onnxruntime


def test_run_pip_streams_launcher_progress(monkeypatch, capsys):
    calls = []

    def fake_run(command, **kwargs):
        calls.append((command, kwargs))
        return subprocess.CompletedProcess(command, 0)

    monkeypatch.setattr(repair_onnxruntime.subprocess, "run", fake_run)

    repair_onnxruntime._run_pip(["install", "example-package"], stream=True)

    assert calls == [
        (
            [
                repair_onnxruntime.sys.executable,
                "-m",
                "pip",
                "--disable-pip-version-check",
                "install",
                "example-package",
            ],
            {"check": True, "text": True},
        )
    ]
    assert "python -m pip --disable-pip-version-check install example-package" in capsys.readouterr().out


def test_core_constraints_file_is_pip26_safe(tmp_path):
    requirements_path = tmp_path / "requirements-core.txt"
    requirements_path.write_text(
        "\n".join(
            [
                "# generated by pip-compile",
                "numpy==1.26.4",
                "uvicorn[standard]==0.46.0",
                "cffi==2.0.0 ; sys_platform == \"win32\"",
                "-r requirements-other.txt",
                "example @ https://example.invalid/example.whl",
            ]
        ),
        encoding="utf-8",
    )

    constraints_path = repair_onnxruntime._write_sanitized_constraints(requirements_path)

    assert constraints_path is not None
    constraints_text = constraints_path.read_text(encoding="utf-8")
    assert "numpy==1.26.4" in constraints_text
    assert "uvicorn==0.46.0" in constraints_text
    assert "uvicorn[standard]" not in constraints_text
    assert 'cffi==2.0.0 ; sys_platform == "win32"' in constraints_text
    assert "-r requirements-other.txt" not in constraints_text
    assert "example @" not in constraints_text


def test_repair_installs_release_pinned_gpu_runtime_from_cpu_only_windows(monkeypatch):
    install_state = {
        "platform": "Windows",
        "python": repair_onnxruntime.sys.executable,
        "onnxruntime_version": "1.20.1",
        "onnxruntime_gpu_version": None,
        "onnxruntime_directml_version": None,
        "has_conflict": False,
        "has_gpu_package": False,
        "has_dml_package": False,
        "gpu_vendor_primary": "nvidia",
        "gpu_vendors_detected": ["nvidia"],
        "gpu_devices": [{"name": "NVIDIA GeForce RTX 4090", "vendor": "nvidia"}],
    }
    pip_calls = []

    def fake_version(dist_name: str):
        versions = {
            "onnxruntime-gpu": "1.21.0",
            "nvidia-cudnn-cu12": None,
        }
        return versions.get(dist_name)

    def fake_run_pip(args, *, stream=False):
        pip_calls.append((args, stream))
        return subprocess.CompletedProcess(args, 0)

    monkeypatch.setattr(repair_onnxruntime, "get_install_state", lambda: dict(install_state))
    monkeypatch.setattr(repair_onnxruntime, "_version", fake_version)
    monkeypatch.setattr(repair_onnxruntime, "_run_pip", fake_run_pip)
    monkeypatch.setattr(repair_onnxruntime, "_probe_ort_providers", lambda: ["CUDAExecutionProvider"] )

    result = repair_onnxruntime.repair_windows_onnxruntime(stream_pip=True)

    assert pip_calls[0] == (["uninstall", "-y", "onnxruntime"], True)
    assert (
        ["install", "--no-deps", "--upgrade", "--force-reinstall", "onnxruntime-gpu==1.21.0"],
        True,
    ) in pip_calls
    cuda_install = next(call for call in pip_calls if "cuda,cudnn" in " ".join(call[0]))
    assert cuda_install[0][:3] == ["install", "--no-warn-script-location", "--constraint"]
    assert cuda_install[0][-1] == "onnxruntime-gpu[cuda,cudnn]==1.21.0"
    assert result["repaired"] is True


