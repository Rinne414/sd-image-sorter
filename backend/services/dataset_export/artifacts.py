logger = logging.getLogger("services.dataset_export_service")


def _mask_export_mode(request: Any) -> str:
    return str(getattr(request, "mask_export", "none") or "none").strip().lower()


def _trainer_config_mode(request: Any) -> str:
    return str(getattr(request, "trainer_config", "none") or "none").strip().lower()


def _toml_path_literal(path: Path) -> str:
    """TOML basic-string value for a filesystem path. Forward slashes keep
    Windows paths escape-free and kohya accepts them."""
    return str(path).replace("\\", "/")


def _write_kohya_dataset_config(
    output_folder: Path,
    request: Any,
    *,
    masks_written: int,
) -> Optional[str]:
    """Drop a ready dataset_config.toml beside the exported pairs.

    Shape follows the official examples (kohya-ss/sd-scripts
    docs/masked_loss_README.md + config_README-en.md): one dataset, one
    subset pointing at the export folder, explicit num_repeats (folder-name
    repeats are ignored by the config method), conditioning_data_dir only
    when kohya-style masks were actually written. Returns the file path, or
    None on failure (reported as an export error message, never a row
    failure — the image/caption pairs on disk are already valid).
    """
    caption_extension = _dataset_sidecar_extension(request.content_mode)
    lines = [
        "# Generated by SD Image Sorter — kohya sd-scripts dataset config.",
        "# Point --dataset_config at this file. Adjust resolution/batch to",
        "# your trainer VRAM; num_repeats here is what the trainer uses",
        "# (folder-name repeats are ignored by the config method).",
        "[general]",
        "enable_bucket = true",
        "",
        "[[datasets]]",
        f"resolution = {int(request.trainer_resolution)}",
        f"batch_size = {int(request.trainer_batch)}",
        "",
        "  [[datasets.subsets]]",
        f'  image_dir = "{_toml_path_literal(output_folder)}"',
        f'  caption_extension = "{caption_extension}"',
        f"  num_repeats = {int(request.trainer_repeats)}",
    ]
    keep_tokens = int(getattr(request, "trainer_keep_tokens", 0) or 0)
    if keep_tokens > 0:
        # docs/config_README-en.md example: shuffle with the first N tokens
        # pinned — the trigger (and any leading common tags) stay first.
        lines.append("  shuffle_caption = true")
        lines.append(f"  keep_tokens = {keep_tokens}")
    if _mask_export_mode(request) == "kohya" and masks_written > 0:
        lines.append(
            f'  conditioning_data_dir = "{_toml_path_literal(output_folder / "mask")}"'
        )
    trigger = str(getattr(request, "trigger", "") or "").strip()
    if trigger:
        lines.append(f'  class_tokens = "{trigger}"')
    lines.append("")
    target = output_folder / "dataset_config.toml"
    try:
        target.write_text("\n".join(lines), encoding="utf-8", newline="\n")
        return str(target)
    except OSError:
        logger.warning("Could not write dataset_config.toml", exc_info=True)
        return None


def _validate_export_request(request: DatasetExportRequest) -> Optional[Path]:
    output_mode = _output_mode(request)
    if output_mode not in VALID_OUTPUT_MODES:
        raise HTTPException(status_code=400, detail=f"Invalid output_mode: {output_mode!r}")
    if _mask_export_mode(request) not in VALID_MASK_EXPORT_MODES:
        raise HTTPException(
            status_code=400,
            detail=f"Invalid mask_export: {getattr(request, 'mask_export', None)!r} "
                   f"(expected one of {', '.join(VALID_MASK_EXPORT_MODES)})",
        )
    if _trainer_config_mode(request) not in VALID_TRAINER_CONFIGS:
        raise HTTPException(
            status_code=400,
            detail=f"Invalid trainer_config: {getattr(request, 'trainer_config', None)!r} "
                   f"(expected one of {', '.join(VALID_TRAINER_CONFIGS)})",
        )
    if request.image_op not in VALID_IMAGE_OPS:
        raise HTTPException(status_code=400, detail=f"Invalid image_op: {request.image_op!r}")
    if request.overwrite_policy not in VALID_OVERWRITE_POLICIES:
        raise HTTPException(status_code=400, detail=f"Invalid overwrite_policy: {request.overwrite_policy!r}")
    if str(request.content_mode or "template").strip().lower() not in VALID_CONTENT_MODES:
        raise HTTPException(status_code=400, detail=f"Invalid content_mode: {request.content_mode!r}")
    if not request.image_ids and not request.image_paths and not request.dataset_scan_tokens:
        raise HTTPException(status_code=400, detail="Must supply image_ids, image_paths, or dataset_scan_tokens.")

    if output_mode == "beside_image":
        return None

    output_folder_norm = normalize_user_path(request.output_folder)
    if not output_folder_norm:
        raise HTTPException(status_code=400, detail="Output folder is required for folder export mode.")
    is_valid, error = validate_folder_path(output_folder_norm, allow_create=True)
    if not is_valid:
        raise HTTPException(status_code=400, detail=error or "Invalid output folder")
    output_path = Path(output_folder_norm)
    output_path.mkdir(parents=True, exist_ok=True)
    return output_path


def _manifest_item(item: DatasetExportItemResult) -> Dict[str, Any]:
    """Flatten a per-row result into the manifest's source->destination record."""
    return {
        "image_id": item.image_id,
        "source_path": item.src_image_path,
        "output_path": item.dst_image_path,
        "caption_path": item.dst_caption_path,
        "skipped_reason": item.skipped_reason,
        "error": item.error,
    }


def _build_export_manifest(
    request: DatasetExportRequest,
    *,
    status: str,
    output_folder: Path,
    output_mode: str,
    caption_extension: str,
    exported: int,
    skipped: int,
    error_count: int,
    total_items: int,
    items: List[DatasetExportItemResult],
    items_truncated: bool,
    generated_at: float,
) -> Dict[str, Any]:
    """Assemble the ``export_manifest.json`` payload from existing run state.

    Every field is sourced from data already computed during the export — no
    new plumbing. ``items`` mirrors the (capped) per-row list surfaced in the
    API response, so ``items_truncated`` flags when it is shorter than
    ``total`` for very large runs.
    """
    return {
        "manifest_version": EXPORT_MANIFEST_VERSION,
        "generated_at": generated_at,
        "generated_at_iso": datetime.fromtimestamp(generated_at, tz=timezone.utc).isoformat(),
        "status": status,
        "output_folder": str(output_folder),
        "settings": {
            "output_mode": output_mode,
            "image_op": request.image_op,
            "overwrite_policy": request.overwrite_policy,
            "naming_pattern": request.naming_pattern,
            "trigger": request.trigger,
            "content_mode": str(request.content_mode or "template").strip().lower(),
            "caption_extension": caption_extension,
            "prefix": request.prefix,
            "normalize_tag_underscores": bool(request.normalize_tag_underscores),
            "common_tags": list(request.common_tags or []),
            "blacklist": list(request.blacklist or []),
        },
        "counts": {
            "total": total_items,
            "exported": exported,
            "skipped": skipped,
            "failed": error_count,
        },
        "items_truncated": items_truncated,
        "items": [_manifest_item(item) for item in items],
    }


def _write_export_manifest(output_folder: Path, manifest: Dict[str, Any]) -> None:
    """Best-effort write of ``export_manifest.json`` into the output folder.

    A manifest write failure must never fail the export — the images and
    captions are already on disk — so every error is logged and swallowed.
    """
    manifest_path = output_folder / EXPORT_MANIFEST_FILENAME
    try:
        with open(manifest_path, "w", encoding="utf-8") as handle:
            json.dump(manifest, handle, ensure_ascii=False, indent=2)
    except Exception as exc:  # noqa: BLE001 - manifest is a best-effort side-effect
        logger.warning(
            "dataset-export: failed to write export manifest %s: %s",
            manifest_path,
            exc,
        )
